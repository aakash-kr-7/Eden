// =============================================================================
// main.dart — Sol App Entry Point with Firebase + Auth Gate
// =============================================================================
//
// AUTH + ONBOARDING GATE PATTERN:
//   Instead of hardcoding which screen to show, we use Firebase's
//   authStateChanges() stream and then resolve whether the signed-in user
//   still needs the first-run encounter flow.
//
//   This means:
//   - First launch: null → LoginScreen
//   - After first login: User → OnboardingScreen → ChatScreen
//   - Later launches: User → ChatScreen
//   - After logout: null → LoginScreen again
//
//   Firebase handles session persistence automatically.
//   The user never has to log in again unless they explicitly sign out.
// =============================================================================

